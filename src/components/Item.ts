class Item {
	readonly id: ItemID;
	readonly name: string;
	readonly category: ItemCategory;
	readonly state: ItemState;

	constructor(id: ItemID, name: string, category: ItemCategory, state: ItemState) {
		this.id = id;
		this.name = name;
		this.category = category;
		this.state = state;
	}
}

